/**
 * Translate location string (city, state, country) to Arabic
 * Handles complex formats like "City, State, Country"
 * 
 * @param location - Location string in English
 * @param lang - Target language ('ar' or 'en')
 * @returns Translated location string
 * 
 * @example
 * translateLocation('Colorado Springs, Colorado, United States', 'ar')
 * // Returns: 'كولورادو سبرينغز, كولورادو, الولايات المتحدة'
 * 
 * translateLocation('Goiânia, Goiás, Brazil', 'ar')
 * // Returns: 'غويانيا, غوياس, البرازيل'
 */
export function translateLocation(location: string | null | undefined, lang: 'ar' | 'en'): string {
    if (!location || lang === 'en') return location || ''

    let translated = location

    // Sort by length (longest first) to avoid partial matches
    // e.g., "New York" should be matched before "York"
    const sortedTranslations = Object.entries(locationTranslations)
        .sort(([a], [b]) => b.length - a.length)

    // Replace all matching locations
    for (const [english, arabic] of sortedTranslations) {
        // Use word boundaries to avoid partial matches
        const regex = new RegExp(`\\b${english.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}\\b`, 'g')
        translated = translated.replace(regex, arabic)
    }

    return translated
}

